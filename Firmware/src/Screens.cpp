void Screens::drawVolumeScreen(Display& display) {
	drawVolume(display, stateInfo.masterVolume, stateInfo.micVolume, HEADPHONES_GLYPH, MIC_GLYPH);
}

void Screens::drawAppsScreen(Display& display) {
	drawVolume(display, stateInfo.leftKnob, stateInfo.rightKnob, CHROME_GLYPH, SPOTIFY_GLYPH);
}

void Screens::drawVolume(
	Display& display, int leftVolume, int rightVolume,
	const unsigned char* leftGlyph, const unsigned char* rightGlyph) 
{
	
	display.u8g2().drawXBMP(8, 4, 32, 32, leftGlyph);
	display.u8g2().drawXBMP(88, 4, 32, 32, rightGlyph);

	if (leftVolume == 0) {
		display.u8g2().drawXBMP(16, 42, 16, 16, MUTE_GLYPH);
	} else {
		display.drawText(
			(String(leftVolume) + "%").c_str(),
			u8g2_font_helvR10_tf,
			55, leftVolume == 100 ? 6 : leftVolume < 10 ? 14 : 10
		);
	}

	if (rightVolume == 0) {
		display.u8g2().drawXBMP(96, 42, 16, 16, MUTE_GLYPH);
	} else {
		display.drawText(
			(String(rightVolume) + "%").c_str(),
			u8g2_font_helvR10_tf,
			55, rightVolume == 100 ? 86 : rightVolume < 10 ? 94 : 90
		);
	}

	display.u8g2().drawFrame(49, 4, 6, 51);
	display.u8g2().drawBox(49, 4 + (51 - (leftVolume * 51 / 100)), 6, leftVolume * 51 / 100);
	display.u8g2().drawFrame(73, 4, 6, 51);
	display.u8g2().drawBox(73, 4 + (51 - (rightVolume * 51 / 100)), 6, rightVolume * 51 / 100);
}

void Screens::drawTrackInfoScreen(Display& display) {
	display.drawScrollingText(stateInfo.songTitle, u8g2_font_helvB12_tf, 15, stateInfo.titleScroll);

	if (strcmp(stateInfo.songArtist, "null") != 0) {
		display.drawScrollingText(stateInfo.songArtist, u8g2_font_helvR10_tf, 35, stateInfo.artistScroll);
	}

	if (!stateInfo.isPlaying)
	{
		display.u8g2().drawXBMP(57, 44, 14, 14, PLAY_GLYPH);
	} else if (stateInfo.totalSeconds > 0) {
		display.u8g2().drawFrame(0, 48, 128, 6);
		display.u8g2().drawBox(0, 48, stateInfo.trackPercentage * 128 / 100, 6);
	}
}

void Screens::drawDisconnectedScreen(Display& display) {
	display.drawText("Waiting for", u8g2_font_helvR10_tf, 25, 40);
	display.drawText("connection...", u8g2_font_helvR10_tf, 45, 40);

	display.u8g2().drawXBMP(0, 14, 32, 32, USB_GLYPH);
}

void Screens::drawConnectedScreen(Display& display) {
	display.drawText("Connected", u8g2_font_helvB10_tr, 35, 40);

	display.u8g2().drawXBMP(0, 14, 32, 32, USB_GLYPH);
}

void Screens::drawLogoScreen(Display& display) {
	display.drawText("v1.0", u8g2_font_helvR10_tf, 55, 100);

	display.u8g2().drawXBMP(40, 8, 48, 48, LOGO_GLYPH);
}